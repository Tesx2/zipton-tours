function initSpeechRecognition() {
    if ("webkitSpeechRecognition" in window || "SpeechRecognition" in window) {
        const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;
        recognition = new SpeechRecognition();
        recognition.continuous = false;
        recognition.interimResults = true; // Enable real-time feedback while speaking
        recognition.lang = "en-US";

        micButton.title = "Click to speak with our Safari AI";
        micButton.setAttribute("aria-label", "Start voice input"); // Set initial label

        recognition.onstart = () => {
            recognition.transcriptReady = false; // Reset flag for new recognition session
            recognition.speechReceived = false;
            isListening = true;
            micButton.classList.add("listening");
            micButton.setAttribute("aria-label", "Stop voice input"); // Update label when listening
            voiceStatus.textContent = "I'm listening... Speak now! 🎤";
            userInput.placeholder = "Speak now...";
        };

        recognition.onend = () => {
            isListening = false;
            micButton.classList.remove("listening");
            micButton.setAttribute("aria-label", "Start voice input"); // Revert label when not listening
            if (!recognition.speechReceived && !recognition.transcriptReady && userInput.value.trim() === "") {
                voiceStatus.textContent = "No speech detected. Tap mic to try again.";
                setTimeout(() => { 
                    voiceStatus.textContent = ""; 
                    userInput.placeholder = placeholders[currentPlaceholder];
                }, 3000);
            } else if (recognition.transcriptReady) {
                setTimeout(() => {
                    if (voiceStatus.textContent.includes("I heard:")) { // Only clear if it's the transcription message
                        voiceStatus.textContent = "";
                    }
                }, 3000); // Keep "I heard" message for 3 seconds
            }
        };

        recognition.onresult = (event) => {
            let transcript = "";
            for (let i = 0; i < event.results.length; i++) {
                transcript += event.results[i][0].transcript;
            }

            userInput.value = transcript;
            recognition.speechReceived = true; // Mark that we heard something immediately

            if (event.results[event.results.length - 1].isFinal) {
                recognition.transcriptReady = true;
                voiceStatus.textContent = `I heard: "${transcript}" ✨`;
            } else {
                voiceStatus.textContent = "Hearing you... 🎤";
            }
        };

        recognition.onerror = (event) => {
            isListening = false;
            micButton.classList.remove("listening");
            voiceStatus.textContent = `Voice error: ${event.error}. Please type your message.`;
            setTimeout(() => { voiceStatus.textContent = ""; }, 5000); // Give user more time to read error
        };
    } else {
        micButton.style.display = "none";
        voiceStatus.textContent = "Speech recognition not supported in this browser. Please type your message.";
    }
}

// Unlock speech synthesis on first user interaction to bypass browser restrictions
function unlockSpeech() {
    if (synthesis) {
        const silentUtterance = new SpeechSynthesisUtterance("");
        synthesis.speak(silentUtterance);
    }
    document.removeEventListener('click', unlockSpeech);
}
document.addEventListener('click', unlockSpeech);

function speak(text) {
    if (!voiceEnabled || !synthesis) return;

    synthesis.cancel();
    // Clean up text: remove emojis but keep newlines for natural pauses
    const cleanText = text.replace(/[\u{1F300}-\u{1F9FF}]|[\u{2700}-\u{27BF}]|[•]/gu, "");
    const utterance = new SpeechSynthesisUtterance(cleanText);
    
    utterance.rate = 1.0; // Standard speed (1.0) sounds more human-like than 0.9
    utterance.pitch = 1.0;
    utterance.volume = 1.0;

    const voices = synthesis.getVoices();
    if (voices.length === 0) {
        // If voices aren't loaded yet, try again briefly
        setTimeout(() => speak(text), 100);
        return;
    }

    // Prioritize high-quality "Natural" (Edge) or "Google" (Chrome) male voices
    const preferredVoice = voices.find(v => v.lang.startsWith("en") && v.name.includes("Natural") && v.name.includes("Male")) ||
                           voices.find(v => v.lang.startsWith("en") && v.name.includes("Google") && v.name.includes("Male")) ||
                           voices.find(v => v.lang.startsWith("en") && v.name.includes("Male")) ||
                           voices.find(v => v.lang.startsWith("en"));

    if (preferredVoice) utterance.voice = preferredVoice;

    synthesis.resume(); // Fixes a bug in Chrome where speech gets stuck
    synthesis.speak(utterance);
}

async function getBotResponse(userMessage) {
    const lowerMessage = userMessage.toLowerCase();

    // Add user message to history
    chatHistory.push({ role: "user", content: userMessage });

    try {
        console.log("Chatbot: Attempting to connect to AI at", API_ENDPOINT);
        const response = await fetch(API_ENDPOINT, {
            method: 'POST',
            headers: { 'Content-Type': 'application/json' },
            body: JSON.stringify({ messages: chatHistory })
        });

        if (!response.ok) {
            const errorData = await response.json().catch(() => ({}));
            throw new Error(errorData.error || `Server error: ${response.status}`);
        }

        const result = await response.json();
        if (result.choices && result.choices[0] && result.choices[0].message) {
            const botText = result.choices[0].message.content;

            // Add AI response to history
            chatHistory.push({ role: "assistant", content: botText });
            
            // Limit history to last 20 messages to keep requests fast
            if (chatHistory.length > 20) chatHistory = chatHistory.slice(-20);

            return botText;
        }
        throw new Error("Received an empty or invalid response from the AI server.");
    } catch (error) {
        console.error("NVIDIA API Failure:", error.message);
        console.info("Using local knowledge base fallback...");

        // Fallback to local keyword matching
        for (const [category, data] of Object.entries(knowledgeBase)) {
            if (category === "default") continue;
            if (data.keywords && data.keywords.some(kw => lowerMessage.includes(kw))) {
                return data.response;
            }
        }
        return knowledgeBase.default.response;
    }
}

function formatTime(date) {
    return date.toLocaleTimeString("en-US", { hour: "numeric", minute: "2-digit" });
}

function addMessage(content, sender) {
    const messageDiv = document.createElement("div");
    messageDiv.className = `message ${sender}-message`;

    const contentDiv = document.createElement("div");
    contentDiv.className = "message-content";
    contentDiv.innerHTML = content;

    const timeDiv = document.createElement("div");
    timeDiv.className = "message-time";
    timeDiv.textContent = formatTime(new Date());

    messageDiv.appendChild(contentDiv);
    messageDiv.appendChild(timeDiv);
    chatContainer.appendChild(messageDiv);
    chatContainer.scrollTop = chatContainer.scrollHeight;
}

