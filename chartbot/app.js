async function handleSend() {
    const message = userInput.value.trim();
    if (!message) return;
    
    // Clear any previous voice status when sending
    voiceStatus.textContent = ""; 
    recognition.transcriptReady = false; // Reset flag as message is being sent

    // If the user sends an empty message after voice input, clear status and re-enable input
    if (!message) {
        userInput.disabled = false;
        sendButton.disabled = false;
        return;
    }

    addMessage(message, "user");
    userInput.value = "";
    
    // Disable input while thinking
    userInput.disabled = true;
    sendButton.disabled = true;
    
    showTypingIndicator();

    const response = await getBotResponse(message);
    removeTypingIndicator();
    addMessage(response, "bot");
    speak(response);

    // Re-enable input
    userInput.disabled = false;
    sendButton.disabled = false;
    userInput.focus();
}

function cyclePlaceholder() {
    currentPlaceholder = (currentPlaceholder + 1) % placeholders.length;
    if (!userInput.matches(":focus")) {
        userInput.placeholder = placeholders[currentPlaceholder];
    }
}

micButton.addEventListener("click", () => {
    if (!recognition) {
        alert("Speech recognition is not supported in your browser. Please use Chrome or Edge.");
        return;
    }

    if (isListening) {
        recognition.stop();
    } else {
        recognition.start();
    }
});

chatForm.addEventListener("submit", (e) => {
    e.preventDefault(); // Prevents the page from reloading
    handleSend();
});


document.addEventListener("keydown", (e) => {
    if (e.ctrlKey || e.metaKey) {
        switch (e.key) {
            case "M":
                e.preventDefault();
                micButton.click();
                break;
            case "Enter":
                e.preventDefault();
                handleSend();
                break;
        }
    }
});

